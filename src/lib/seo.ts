/** Build FAQPage schema from H2 headings that look like questions. */
export function extractFaqQuestions(bodyMdx: string): Array<{
  question: string;
  answer: string;
}> {
  const lines = bodyMdx.split("\n");
  const faqs: Array<{ question: string; answer: string }> = [];
  let currentQ: string | null = null;
  let currentAnswer: string[] = [];
  let inFence = false;
  for (const line of lines) {
    if (line.trimStart().startsWith("```")) {
      inFence = !inFence;
      if (currentQ) currentAnswer.push(line);
      continue;
    }
    if (inFence) {
      if (currentQ) currentAnswer.push(line);
      continue;
    }
    const m = /^##\s+(.+?)\s*$/.exec(line);
    if (m) {
      if (currentQ) {
        const answer = currentAnswer.join("\n").trim();
        if (answer) faqs.push({ question: currentQ, answer });
      }
      const heading = m[1].trim();
      currentQ = isQuestionLike(heading) ? heading : null;
      currentAnswer = [];
      continue;
    }
    if (currentQ) currentAnswer.push(line);
  }
  if (currentQ) {
    const answer = currentAnswer.join("\n").trim();
    if (answer) faqs.push({ question: currentQ, answer });
  }
  return faqs;
}

function isQuestionLike(heading: string): boolean {
  // Plain question mark, or starts with common interrogative words EN/VI.
  if (heading.endsWith("?")) return true;
  return /^(what|how|why|when|where|which|who|cách|tại sao|là gì|gì\b)/i.test(
    heading,
  );
}

export function buildFaqJsonLd(
  questions: Array<{ question: string; answer: string }>,
) {
  if (questions.length === 0) return null;
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: questions.map((q) => ({
      "@type": "Question",
      name: q.question,
      acceptedAnswer: { "@type": "Answer", text: markdownToPlain(q.answer) },
    })),
  };
}

function markdownToPlain(s: string): string {
  return s
    .replace(/```[\s\S]*?```/g, "")
    .replace(/`([^`]+)`/g, "$1")
    .replace(/\*\*([^*]+)\*\*/g, "$1")
    .replace(/\*([^*]+)\*/g, "$1")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/^\s*[#>\-*]\s*/gm, "")
    .replace(/\n{2,}/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 2000);
}
